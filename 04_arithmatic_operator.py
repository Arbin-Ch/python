# Arithmetic Operator = They are used with numeric values to perform common mathematical operations.

# Use any of them:
# Addition operator
friends = 10

friends += 1                    # Assignment operators
# friends = friends + 1
print(friends)

# Subtraction operator
friends = 10

friends -= 1                    # Assignment operators
# friends = friends - 1
print(friends)

# Multiplication operator
friends = 10

friends *= 2                    # Assignment operators
# friends = friends * 2
print(friends)

# Division Operator
friends = 10

friends /= 2                    # Assignment operators
# friends = friends / 2
print(friends)

# Exponentiation operator
friends = 10

friends **= 2                    # Assignment operators
# friends = friends ** 2
print(friends)
# (10 * 10)

# Floor Division Operator
friends = 10

friends //= 3                   # Assignment operators
# friends = friends // 3
print(friends)
# prints whole number (15//2=7)

# Modulus operator
Remainder = 5

Remainder %= 2                    # Assignment operators
# Remainder = Remainder % 2
print(Remainder)
# prints the remainder